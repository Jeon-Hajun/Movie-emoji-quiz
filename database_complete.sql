-- 기존 테이블 삭제 (재생성 시)
DROP TABLE IF EXISTS score;
DROP TABLE IF EXISTS quiz;

-- 퀴즈 문제 테이블 생성
CREATE TABLE IF NOT EXISTS quiz (
    id INT AUTO_INCREMENT PRIMARY KEY,
    emoji TEXT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci NOT NULL,
    answer VARCHAR(255) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci NOT NULL,
    poster VARCHAR(255) NOT NULL,
    hint VARCHAR(255) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

-- 점수 기록 테이블 생성 (리더보드용) - 모든 기록 저장
CREATE TABLE IF NOT EXISTS score (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(50) NOT NULL
    score INT NOT NULL
    record_number INT NOT NULL
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_score_score (score DESC),
    INDEX idx_score_name (name),
    INDEX idx_score_record (name, record_number)
) CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

